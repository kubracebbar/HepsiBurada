package rover;


public class Plateau {
    private int mX, mY;

    public Plateau(int mX, int mY) {
        this.mX = mX;
        this.mY = mY;
    }

    public int getmX() {
        return mX;
    }

    public void setmX(int mX) {
        this.mX = mX;
    }

    public int getmY() {
        return mY;
    }

    public void setmY(int mY) {
        this.mY = mY;
    }


}
